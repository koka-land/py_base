chislo = abs(int(input()))
chislo_str = str(chislo)

if len(chislo_str) == 4:
    ##############################################
    print('Число чётное:', end=' ')
    if chislo % 2 == 0:
        print('Да')
    else:
        print('Нет')
    ##############################################
    print('Число кратное трём:', end=' ')
    if chislo % 3 == 0:
        print('Да')
    else:
        print('Нет')
    ##############################################
    print('Число является квадратом числа:', end=' ')
    if (chislo ** 0.5) == int(chislo ** 0.5):
        print(int(chislo ** 0.5))
    else:
        print('Нет')
    ##############################################
    print()
    ##############################################
    sp = list(map(int, chislo_str))
    print('Наименьшая цифра числа:', min(sp))
    print('Наибольшая цифра числа:', max(sp))
    print('Количество нулей в числе:', sp.count(0))
    ##############################################
else:
    print('Введённое число не является четырёхзначным')
